/**
 * 강아지 MBTI 검사 질문
 * 형식: "질문 내용/알파벳" - 슬래시 뒤 알파벳(A/L/H/I/S/B/R/F)별로 점수 합산
 * 각 축에서 점수가 높은 성향이 결과에 반영됨
 *
 * A(Active) / L(Low-energy)
 * H(Human-oriented) / I(Independent)
 * S(Sensitive) / B(Bold)
 * R(Routine) / F(Flexible)
 */

function parseQuestion(str: string) {
  const lastSlash = str.lastIndexOf("/");
  if (lastSlash === -1) return { text: str, type: "?" };
  const text = str.slice(0, lastSlash).trim();
  const type = str.slice(lastSlash + 1).trim();
  return type ? { text, type: type.toUpperCase() } : { text: str, type: "?" };
}

